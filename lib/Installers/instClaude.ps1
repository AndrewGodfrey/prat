# Installs Claude Code user-level CLAUDE.md, assembled from a base fragment (in prat) plus optional additional fragments.
function Install-ClaudeUserConfig {
    [CmdletBinding()]
    param ($stage, [string[]] $fragmentPaths)
    $destFile = "$home\.claude\CLAUDE.md"

    $content = "<!-- Auto-generated by Prat deployment. Edit the source fragments, not this file. -->`n`n"

    $content += Import-TextFile "$PSScriptRoot\..\agents\agent-user.md"

    foreach ($path in $fragmentPaths) {
        $content += "`n`n"
        $content += Import-TextFile $path
    }

    Install-TextToFile $stage $destFile $content -SetReadOnly -ShowUpdateDetails:($VerbosePreference -eq 'Continue')
}

# Install a named set of Claude Code skills from a source directory of skills into a target .claude/skills directory.
# Each subdirectory in $srcDir is treated as one skill and may be deployed to the corresponding subdirectory in $destDir.
# Deployed files get an auto-generated header and are set read-only to discourage editing the deployed copy.
function Install-ClaudeSkillSet($stage, $set, [string] $srcDir, [string] $destDir) {
    $header = "<!-- Auto-generated by Prat deployment. Edit the source skill file, not this deployed copy. -->"
    Install-Folder $stage $destDir
    foreach ($skillDir in Get-ChildItem -Directory $srcDir) {
        if ($set -contains $skillDir.Name) {
            $destSkillDir = Join-Path $destDir $skillDir.Name
            Install-Folder $stage $destSkillDir
            foreach ($file in Get-ChildItem -File $skillDir.FullName) {
                $content = Import-TextFile (Join-Path $skillDir.FullName $file.Name)
                if ($content -match '(?s)^(---\r?\n.*?\r?\n---\r?\n)') {
                    $frontmatter = $matches[1]
                    $body = $content.Substring($frontmatter.Length)
                    $newContent = $frontmatter + "`n" + $header + "`n`n" + $body
                } else {
                    $newContent = $header + "`n`n" + $content
                }
                Install-TextToFile $stage (Join-Path $destSkillDir $file.Name) $newContent -SetReadOnly
            }
        }
    }
}

# Install Claude Code agents or commands from a flat directory of .md files into a target
# .claude/agents or .claude/commands directory.
# Places the auto-generated header after YAML frontmatter (if present) so that YAML parsing
# is not broken by a leading comment.
function Install-ClaudeMarkdownFiles($stage, [string] $srcDir, [string] $destDir) {
    $header = "<!-- Auto-generated by Prat deployment. Edit the source file, not this deployed copy. -->"
    Install-Folder $stage $destDir
    foreach ($file in Get-ChildItem -File $srcDir) {
        $content = Import-TextFile (Join-Path $srcDir $file.Name)
        if ($content -match '(?s)^(---\r?\n.*?\r?\n---\r?\n)') {
            $frontmatter = $matches[1]
            $body = $content.Substring($frontmatter.Length)
            $newContent = $frontmatter + "`n" + $header + "`n`n" + $body
        } else {
            $newContent = $header + "`n`n" + $content
        }
        Install-TextToFile $stage (Join-Path $destDir $file.Name) $newContent -SetReadOnly
    }
}

# Sync selected .claude subdirectories to a sync folder (OneDrive, Dropbox, etc.)
# using directory junctions.
#
# This lets conversation history, plans, memory, etc. be backed up and shared across machines,
# while keeping credentials, machine-specific data, and ephemeral data local.
#
# $syncRoot: The sync folder root for claude data, e.g. "$home\OneDrive\.claude-sync"
# $claudeDir: Override for testing. Defaults to "$home\.claude".
function Install-ClaudeSyncFolders($stage, [string] $syncRoot, [string] $claudeDir = "$home\.claude") {
    # Note: session transcripts in projects/ contain references to file-history/ (for undo/rewind),
    # which is local-only. This is benign — rewind won't work for sessions from another machine,
    # but conversation history, memory, and context are unaffected.
    $syncDirs = @("projects", "tasks", "todos", "plans")

    # file-history: undo/rewind snapshots - local file contents, not portable across machines
    $knownLocalDirs = @(
        "agents", "backups", "cache", "commands", "debug", 
        "downloads", "file-history", "ide", "image-cache", "paste-cache", 
        "plugins", "session-env", "shell-snapshots", "skills",
        "statsig", "telemetry"
    )

    Install-SyncFolders $stage ".claude" $claudeDir $syncRoot $syncDirs $knownLocalDirs
}

# Installs a read-only MEMORY.md into every Claude Code project memory directory found under $claudeDir,
# redirecting the auto-memory feature to source-controlled files instead.
#
# Won't overwrite an existing custom MEMORY.md - instead will warn and skip.
function Install-ClaudeProjectMemory {
    param($stage, [string] $claudeDir = "$home\.claude")

    $header = "<!-- Auto-generated by Prat deployment. Do not edit — see source. -->"
    $content = $header + "`n`n" + (Import-TextFile "$PSScriptRoot\..\agents\memory-template.md")

    $projectsDir = Join-Path $claudeDir "projects"
    if (-not (Test-Path $projectsDir)) { return }

    foreach ($projectDir in Get-ChildItem -Directory $projectsDir -ErrorAction SilentlyContinue) {
        $memoryDir  = Join-Path $projectDir.FullName "memory"
        $memoryFile = Join-Path $memoryDir "MEMORY.md"

        if (Test-Path $memoryFile) {
            $existing = Get-Content $memoryFile -Raw -ErrorAction SilentlyContinue
            if ($existing -notlike "$header*") {
                Write-Warning "Skipping $memoryFile — has non-generated content. Review and delete manually."
                continue
            }
        }

        Install-Folder $stage $memoryDir
        Install-TextToFile $stage $memoryFile $content -SetReadOnly
    }
}

# Installs Claude Code user-level settings.json. For now just a copy, may need merging later.
# Wraps Install-LocalAgentSandbox with Claude-specific config sharing: junction for .claude/,
# symlinks for .claude.json and .claude.json.backup. The two json files are automatically added
# to rwPaths so permissions are granted and {} placeholders created if not yet present.
#
# .PARAMETER claudeHome
# Home directory of the user whose Claude config the agent should share.
function Install-ClaudeAgentSandbox {
    [CmdletBinding()]
    param(
        $stage,
        [string] $agentUser,
        [string] $claudeHome,
        [string[]] $rwPaths = @(),
        [string[]] $roPaths = @(),
        [string[]] $safeDirectories = @(),
        [hashtable] $homeJunctions = @{},
        [string] $profileContent = $null,
        [string] $sshPublicKeyPath = $null
    )

    $claudeHome = $claudeHome -replace '/', '\'

    Install-LocalAgentSandbox $stage `
        -agentUser       $agentUser `
        -rwPaths         ($rwPaths + @("$claudeHome\.claude", "$claudeHome\.claude.json", "$claudeHome\.claude.json.backup")) `
        -roPaths         ($roPaths + @("$claudeHome\.local\bin")) `
        -safeDirectories $safeDirectories `
        -homeJunctions   $homeJunctions `
        -profileContent  $profileContent `
        -sshPublicKeyPath $sshPublicKeyPath

    # Create junction and symlinks after Install-LocalAgentSandbox has set ACLs and ensured targets exist.
    $agentHome = "$env:SystemDrive\Users\$agentUser"

    $jLink = "$agentHome\.claude"
    $jItem = Get-Item $jLink -ErrorAction SilentlyContinue
    if ($null -eq $jItem -or $jItem.LinkType -ne 'Junction') {
        $stage.OnChange()
        $jTarget = "$claudeHome\.claude"
        if ($null -ne $jItem) { Invoke-Gsudo { Remove-Item -Force -Recurse $using:jLink } }
        Invoke-Gsudo { New-Item -ItemType Junction -Path $using:jLink -Target $using:jTarget | Out-Null }
    }

    foreach ($fileName in @('.claude.json', '.claude.json.backup')) {
        $sLink = "$agentHome\$fileName"
        $sItem = Get-Item $sLink -ErrorAction SilentlyContinue
        if ($null -eq $sItem -or $sItem.LinkType -ne 'SymbolicLink') {
            $stage.OnChange()
            $sTarget = "$claudeHome\$fileName"
            if ($null -ne $sItem) { Invoke-Gsudo { Remove-Item -Force $using:sLink } }
            Invoke-Gsudo { New-Item -ItemType SymbolicLink -Path $using:sLink -Target $using:sTarget | Out-Null }
        }
    }
}

function Install-ClaudeUserSettings($stage, [string] $sourceFile) {
    $destFile = "$home\.claude\settings.json"

    $json = ConvertFrom-Json (Import-TextFile $sourceFile) -AsHashtable
    $json["_comment"] = "<!-- Auto-generated by Prat deployment. Edit the source file - $sourceFile, not this file."
    $newText = ConvertTo-Json $json -Depth 20

    Install-TextToFile $stage $destFile $newText -SetReadOnly
}

