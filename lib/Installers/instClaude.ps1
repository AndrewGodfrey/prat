# Installs Claude Code user-level CLAUDE.md, assembled from registered fragments.
function Install-ClaudeUserInstructions {
    [CmdletBinding()]
    param ($stage, [string[]] $fragmentPaths)
    Install-HarnessUserInstructions $stage "$home\.claude\CLAUDE.md" $fragmentPaths
}

# Installs all Claude Code harness integration: instructions, settings/hooks, and optional sync/memory.
# Called via Install-HarnessIntegration $stage 'claude'.
function Install-ClaudeHarness {
    param($stage, [string[]] $Suppress = @(), [string[]] $Enable = @(), [hashtable] $Config = @{})

    Install-Folder $stage "$home\.claude"

    if ('installHomeClaudeMd' -notin $Suppress) {
        $fragments = @(Get-HarnessUserFragments)

        # Splice prat-cc.md after the prat base (index 0) so CC-specific prat content
        # precedes higher-layer (prefs/de) fragments.
        $pratCcFile = "$PSScriptRoot\..\agents\harness-specific\prat-cc.md"
        if (Test-Path $pratCcFile) {
            $rest      = if ($fragments.Count -gt 1) { $fragments[1..($fragments.Count - 1)] } else { @() }
            $fragments = @($fragments[0], $pratCcFile) + $rest
        }

        Install-ClaudeUserInstructions $stage $fragments
    }

    Install-ClaudeUserSettings $stage

    if ('installClaudeSyncFolders' -in $Enable -and $Config.syncFoldersPath) {
        Install-ClaudeSyncFolders $stage $Config.syncFoldersPath
    }

    if ('installClaudeProjectMemory' -in $Enable) {
        Install-ClaudeProjectMemory $stage
    }
}

# Install Claude Code skills into a target .claude/skills directory.
# Deployed files get an auto-generated header and are set read-only to discourage editing the deployed copy.
# Skills not in any set are removed from $destDir.
#
#   Install-ClaudeSkillSet $stage @(
#       @{ set = @("skill-a"); srcDir = "path/to/skills1" }
#       @{ set = @("skill-b"); srcDir = "path/to/skills2" }
#   ) $destDir
function Install-ClaudeSkillSet($stage, $sources, [string] $destDir) {
    $headerText = "Auto-generated by Prat deployment. Edit the source skill file, not this deployed copy."
    $htmlHeader = "<!-- $headerText -->"
    $hashHeader = "# $headerText"

    $entries = @($sources)
    $allNames = @()
    foreach ($entry in $entries) {
        $entrySet = $entry.set
        $entrySrcDir = $entry.srcDir
        $srcNames = (Get-ChildItem -Directory $entrySrcDir).Name
        foreach ($name in $entrySet) {
            if ($name -notin $srcNames) { throw "Skill '$name' not found in $entrySrcDir" }
        }
        $allNames += $entrySet
    }

    Install-Folder $stage $destDir
    foreach ($entry in $entries) {
        foreach ($skillDir in Get-ChildItem -Directory $entry.srcDir) {
            if ($entry.set -contains $skillDir.Name) {
                $destSkillDir = Join-Path $destDir $skillDir.Name
                Install-Folder $stage $destSkillDir
                foreach ($file in Get-ChildItem -File $skillDir.FullName) {
                    # Skip test files; they're for source-side validation, not deployment.
                    if ($file.Name -like '*.Tests.ps1') { continue }

                    # Pick comment syntax by extension. PowerShell and Python need `#`; markdown/HTML use `<!-- -->`.
                    $header = switch -Wildcard ($file.Extension.ToLower()) {
                        '.ps1'  { $hashHeader }
                        '.psm1' { $hashHeader }
                        '.psd1' { $hashHeader }
                        '.py'   { $hashHeader }
                        default { $htmlHeader }
                    }

                    $content = Import-TextFile (Join-Path $skillDir.FullName $file.Name)
                    if ($content -match '(?s)^(---\r?\n.*?\r?\n---\r?\n)') {
                        $frontmatter = $matches[1]
                        $body = $content.Substring($frontmatter.Length)
                        $newContent = $frontmatter + "`n" + $header + "`n`n" + $body
                    } else {
                        $newContent = $header + "`n`n" + $content
                    }
                    Install-TextToFile $stage (Join-Path $destSkillDir $file.Name) $newContent -SetReadOnly
                }
            }
        }
    }
    foreach ($destSkillDir in Get-ChildItem -Directory $destDir -ErrorAction SilentlyContinue) {
        if ($destSkillDir.Name -notin $allNames) {
            $stage.OnChange()
            Remove-Item $destSkillDir.FullName -Recurse -Force
        }
    }
}

# Resolve layered agent-role definitions into a flat per-role skill map.
#
# Each layer contributes a `lib/agents/roles_<layer>.ps1` returning
#   @{ skillGroups = @{ <groupName> = @(<skill>...) }; roles = @{ <roleName> = @{ skillGroups=@(...); skills=@(...); repo=... } } }
# Lower layers (prat) define skill groups; the top layer (de) composes roles from them.
# Groups and roles merge base-first, so a higher layer wins on a name collision.
#
# Returns @{ <roleName> = @{ skills = @(<resolved skill names>); repo = <optional> } }.
# Test seams: -Contributions injects already-evaluated definitions (base-first); -RolesFiles injects
# the file list (highest-first, as Resolve-PratLibFile returns it). Normally both are read from disk.
function Get-AgentRoles {
    param([object[]] $Contributions, [string[]] $RolesFiles)

    if (-not $PSBoundParameters.ContainsKey('Contributions')) {
        if (-not $PSBoundParameters.ContainsKey('RolesFiles')) {
            $RolesFiles = @(Resolve-PratLibFile 'lib/agents/roles.ps1' -ListAll)   # highest-first
        }
        $files = @($RolesFiles)
        [array]::Reverse($files)                                                  # base-first
        $Contributions = @($files | ForEach-Object { & $_ })
    }

    $mergedGroups = @{}
    $mergedRoles  = @{}
    foreach ($c in $Contributions) {
        if ($c.skillGroups) { foreach ($k in $c.skillGroups.Keys) { $mergedGroups[$k] = $c.skillGroups[$k] } }
        if ($c.roles)       { foreach ($k in $c.roles.Keys)       { $mergedRoles[$k]  = $c.roles[$k]  } }
    }

    $resolved = @{}
    foreach ($roleName in $mergedRoles.Keys) {
        $role = $mergedRoles[$roleName]
        $skills = @()
        if ($role.skillGroups) {
            foreach ($g in $role.skillGroups) {
                if (-not $mergedGroups.ContainsKey($g)) { throw "Role '$roleName' references unknown skillGroup '$g'" }
                $skills += $mergedGroups[$g]
            }
        }
        if ($role.skills) { $skills += $role.skills }

        $entry = @{ skills = @($skills | Select-Object -Unique) }
        if ($role.repo) { $entry.repo = $role.repo }
        $resolved[$roleName] = $entry
    }
    return $resolved
}

# Deploy per-role skill dirs to <destParent>/<roleName>/.claude/skills/.
# Skill sources are discovered from the installed layers (prat -> prefs -> de); each role's skills
# are grouped by the source dir that provides them and handed to Install-ClaudeSkillSet.
# Throws if a role names a skill that no source provides (catches typos and source drift).
# Role dirs under $destParent that aren't in $roles are removed.
#
# $roles is the resolved map from Get-AgentRoles: @{ <roleName> = @{ skills = @(...); repo = ... } }.
# Test seam: -skillSources injects the base-first source dir list (normally discovered from the layers).
function Install-AgentRoles($stage, [hashtable] $roles, [string] $destParent = "$home/agentRoles", [string[]] $skillSources) {
    if (-not $PSBoundParameters.ContainsKey('skillSources')) {
        # Skill source dirs in base-first order (prat -> prefs -> de)
        $layers = @(Get-CodebaseLayers)
        [array]::Reverse($layers)
        $skillSources = @($layers | ForEach-Object {
            $dir = Join-Path $_.Path 'lib/agents/skills'
            if (Test-Path $dir) { $dir }
        } | Where-Object { $_ })
    }

    foreach ($roleName in $roles.Keys) {
        $role = $roles[$roleName]
        $roleDest = Join-Path $destParent $roleName

        $sources = @()
        $matchedAll = @()
        foreach ($srcDir in $skillSources) {
            $available = @(Get-ChildItem -Directory $srcDir -ErrorAction SilentlyContinue).Name
            $matched = @($role.skills | Where-Object { $_ -in $available })
            if ($matched.Count -gt 0) {
                $sources += @{ set = $matched; srcDir = $srcDir }
                $matchedAll += $matched
            }
        }

        $unmatched = @($role.skills | Where-Object { $_ -notin $matchedAll })
        if ($unmatched.Count -gt 0) {
            throw "Role '$roleName' names skills with no source dir: $($unmatched -join ', ')"
        }

        Install-ClaudeSkillSet $stage $sources "$roleDest/.claude/skills"
    }

    # Remove role dirs no longer defined.
    foreach ($existing in Get-ChildItem -Directory $destParent -ErrorAction SilentlyContinue) {
        if ($existing.Name -notin $roles.Keys) {
            $stage.OnChange()
            Remove-Item $existing.FullName -Recurse -Force
        }
    }
}

# Install Claude Code agents or commands from a flat directory of .md files into a target
# .claude/agents or .claude/commands directory.
# Places the auto-generated header after YAML frontmatter (if present) so that YAML parsing
# is not broken by a leading comment.
function Install-ClaudeMarkdownFiles($stage, [string] $srcDir, [string] $destDir, [switch] $Cleanup) {
    $header = "<!-- Auto-generated by Prat deployment. Don't edit this copy - any edits will just be overwritten. -->"
    Install-Folder $stage $destDir
    foreach ($file in Get-ChildItem -File $srcDir) {
        $content = Import-TextFile (Join-Path $srcDir $file.Name)
        if ($content -match '(?s)^(---\r?\n.*?\r?\n---\r?\n)') {
            $frontmatter = $matches[1]
            $body = $content.Substring($frontmatter.Length)
            $newContent = $frontmatter + "`n" + $header + "`n`n" + $body
        } else {
            $newContent = $header + "`n`n" + $content
        }
        Install-TextToFile $stage (Join-Path $destDir $file.Name) $newContent -SetReadOnly
    }
    if ($Cleanup) {
        $sourceNames = (Get-ChildItem -File $srcDir).Name
        foreach ($destFile in Get-ChildItem -File $destDir -ErrorAction SilentlyContinue) {
            if ($destFile.Name -notin $sourceNames) {
                $stage.OnChange()
                Set-ItemProperty $destFile.FullName -Name IsReadOnly -Value $false -ErrorAction SilentlyContinue
                Remove-Item $destFile.FullName -Force
            }
        }
    }
}

# Sync the .claude/plan folder to a sync folder (OneDrive, Dropbox, etc.)
# using a directory junction.
#
# $syncRoot: The sync folder root for claude data, e.g. "$home\OneDrive\.claude-sync"
# $claudeDir: Override for testing. Defaults to "$home\.claude".
function Install-ClaudeSyncFolders($stage, [string] $syncRoot, [string] $claudeDir = "$home\.claude") {
    $syncDirs = @("plans")

    Install-SyncFolders $stage ".claude" $claudeDir $syncRoot $syncDirs
}

# Installs a read-only MEMORY.md into every Claude Code project memory directory found under $claudeDir,
# redirecting the auto-memory feature to source-controlled files instead.
#
# Won't overwrite an existing custom MEMORY.md - instead will warn and skip.
function Install-ClaudeProjectMemory {
    param($stage, [string] $claudeDir = "$home\.claude")

    $header = "<!-- Auto-generated by Prat deployment. Do not edit — see source. -->"
    $content = $header + "`n`n" + (Import-TextFile "$PSScriptRoot\..\agents\memory-template.md")

    $projectsDir = Join-Path $claudeDir "projects"
    if (-not (Test-Path $projectsDir)) { return }

    foreach ($projectDir in Get-ChildItem -Directory $projectsDir -ErrorAction SilentlyContinue) {
        $memoryDir  = Join-Path $projectDir.FullName "memory"
        $memoryFile = Join-Path $memoryDir "MEMORY.md"

        if (Test-Path $memoryFile) {
            $existing = Get-Content $memoryFile -Raw -ErrorAction SilentlyContinue
            if ($existing -notlike "$header*") {
                Write-Warning "Skipping $memoryFile — has non-generated content. Review and delete manually."
                continue
            }
        }

        Install-Folder $stage $memoryDir
        Install-TextToFile $stage $memoryFile $content -SetReadOnly
    }
}

# This can be removed, in favor of Install-LocalAgentSandbox, once the migration steps can be removed.
# It used to wrap Install-LocalAgentSandbox with CC-specific migration cleanup (junction/symlink/ACE removal).
#
# .PARAMETER claudeHome
# Home directory of the primary user (used for migration cleanup targets).
function Install-ClaudeAgentSandbox {
    [CmdletBinding()]
    param(
        $stage,
        [string] $agentUser,
        [string] $claudeHome,
        [string[]] $rwPaths = @(),
        [string[]] $roPaths = @(),
        [string[]] $safeDirectories = @(),
        [hashtable] $homeJunctions = @{},
        [string] $profileContent = $null,
        [string] $sshPublicKeyPath = $null
    )

    $claudeHome = $claudeHome -replace '/', '\'

    Install-LocalAgentSandbox $stage `
        -agentUser       $agentUser `
        -rwPaths         $rwPaths `
        -roPaths         $roPaths `
        -safeDirectories $safeDirectories `
        -homeJunctions   $homeJunctions `
        -profileContent  $profileContent `
        -sshPublicKeyPath $sshPublicKeyPath

    # Create symlinks after Install-LocalAgentSandbox has set ACLs and ensured targets exist.
    $agentHome = "$env:SystemDrive\Users\$agentUser"

    # Migration: .claude junction management moved to de layer. Remove prat-managed junction.
    $stage.NoteMigrationStep((Get-Date "2026-06-27"))
    $jLink = "$agentHome\.claude"
    $jItem = Get-Item $jLink -ErrorAction SilentlyContinue
    if ($null -ne $jItem -and $jItem.LinkType -eq 'Junction') {
        $stage.OnChange()
        Invoke-Gsudo { Remove-Item -Force -Recurse $using:jLink }
    }

    # Migration: remove .claude.json.backup symlink (no longer running CC from sandbox account).
    $stage.NoteMigrationStep((Get-Date "2026-06-28"))
    $sLink = "$agentHome\.claude.json.backup"
    $sItem = Get-Item $sLink -ErrorAction SilentlyContinue
    if ($null -ne $sItem -and $sItem.LinkType -eq 'SymbolicLink') {
        $stage.OnChange()
        Invoke-Gsudo { Remove-Item -Force $using:sLink }
    }

    # Migration: .local\bin junction management moved to de layer. Remove prat-managed junction.
    $stage.NoteMigrationStep((Get-Date "2026-06-27"))
    $localBin = "$agentHome\.local\bin"
    $jItem = Get-Item $localBin -ErrorAction SilentlyContinue
    if ($null -ne $jItem -and $jItem.LinkType -eq 'Junction') {
        $stage.OnChange()
        Remove-Item -Force -Recurse $localBin
    }

    # Migration: revoke (AD) ACE on claudeHome granted so the agent could create .claude.lock.
    # CC creates .claude.lock as the running user, not the agent, so this right is no longer needed.
    $stage.NoteMigrationStep((Get-Date "2026-06-28"))
    if ($stage.GetIsStepComplete("claudeAgentSandbox/lockGrant/$agentUser")) {
        $stage.OnChange()
        Invoke-Gsudo { icacls $using:claudeHome /remove:g $using:agentUser | Out-Null }
    }
}

function Install-ClaudeUserSettings($stage, [string] $claudeDir = "$home\.claude") {
    $destFile = "$claudeDir\settings.json"

    $sourceFiles = @(Resolve-PratLibFile "lib/inst/Get-ClaudeUserSettings.ps1" -ListAll)
    if ($sourceFiles.Count -eq 0) { throw "No Get-ClaudeUserSettings scripts found in dev environment chain" }

    $merged = @{}
    foreach ($file in $sourceFiles) {
        $layer = & $file
        $merged = Merge-DeepHashtable $merged $layer
    }

    $merged["_comment"] = "Auto-generated by Prat deployment. Edit the source scripts, not this file."
    $newText = ConvertTo-Json $merged -Depth 20

    # We don't set read-only on this file, because Claude Code wants to write it - it records UI/session changes.
    Install-JsonToFile $stage $destFile $newText
}

